/* Задание 1
Создайте функцию, которая возводит переданное число в куб. Hеобходимо вывести в консоль
результат 2^3 степени + 3 ^ 3 степени. */

function cube(num1, num2) {
    return num1 ** 3 + num2 ** 3    
}
console.log(cube(2, 3));


