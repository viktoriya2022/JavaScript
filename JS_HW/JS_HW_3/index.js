/* Задание 3
Пользователь с клавиатуры вводит 3 числа. Необходимо создать функцию, которая определяет максимальное значение среди этих чисел. */

function getMaxNumber(firstNumber, secondNumber, thirdNumber) {
    return `Максимальное число из введенных: ${Math.max(firstNumber, secondNumber, thirdNumber)}`;
}
const firstNumber = prompt(`Введите первое число.`);
const secondNumber = prompt(`Введите второе число.`);
const thirdNumber = prompt(`Введите третье число.`);
console.log(getMaxNumber(firstNumber, secondNumber, thirdNumber));


